package curso_programacao;


public class processamentodados {

	public static void main(String[] args) {
		
		int a, b;
		double resultado;
		
		a = 5;
		b = 2;
		//o uso do double entre parênteses a seguir serve para fazer o casting, que é a conversão explícita
		//da variável a em número flutuante.
		resultado = (double) a / b;
		
		
		System.out.println(resultado);
		
			
	}

}
